/**
 * 自动配置图片URL,约定对象的key值即为图片文件的名字
 */
const _urlPrefix = 'http://wxoss.slhdb2b.com/'
const _fileType = '.png'
function generateUrls(
  imagesObj: object,
  urlPrefix: string = _urlPrefix,
  fileType: string = _fileType
) {
  const keys = Object.getOwnPropertyNames(imagesObj)
  keys.forEach((key: string) => {
    const value = imagesObj[key]
    if (typeof value === 'object') {
      generateUrls(value)
    } else if (value === '') {
      // 默认除了object就是string类型
      imagesObj[key] = `${urlPrefix}${key}${fileType}`
    }
  })
}

